    private void fillEmployeesCMB() throws Exception
    {
        List<Employee> zaposleni = new EmployeeController().getList(false);

        /*cmbZaposleni.addItem("<nije izabran>");
        cmbZaposleni.setSelectedIndex(0);*/
        cmbEmployees.addItem(new Employee());

        for (Employee z : zaposleni)
        {
            cmbEmployees.addItem(z);
            Employee e = projectItem.getEmployee();
            if (e != null && z.getId() == e.getId())
            {
                cmbEmployees.setSelectedItem(z);
            }
        }

    }

    private void fillTasksCMB() throws Exception
    {
        List<Task> zadaci = new TaskController().getList(false);

        //cmbZadatak.addItem("<nije izabran>");
        cmbTasks.addItem(new Task());

        for (Task z : zadaci)
        {
            cmbTasks.addItem(z);
            Task t = projectItem.getTask();
            if (t != null && z.getId() == t.getId())
            {
                cmbTasks.setSelectedItem(z);
                txtOpis.setText(z.getDescription());
            }
        }

    }
